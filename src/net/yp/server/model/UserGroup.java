package net.yp.server.model;

/**
 * 联系人分组
 * @author Eric
 *
 */
public class UserGroup {
	private String id;
	private String name;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
